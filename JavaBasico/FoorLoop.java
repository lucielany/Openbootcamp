package com.company;

public class FoorLoop {                    ////ESTRUCTURAS DE CONTROL DE REPETICION


    public static void main(String[] args) {


        for (int i = 0; i <= 10; i++){              //índice - condicion de parada - condición de paso (incremento)
            System.out.println(i + " Hola mundo");
        }
        System.out.println("fin");
    }
}
